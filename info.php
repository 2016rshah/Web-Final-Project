	<div id="main">
	<h3>Basics</h3>
		<ul>
			<li>Click anywhere on the canvas to create a dot</li>
			<li>Select a dot by clicking on it, or dragging a rectangle over it</li>
			<li>Hold the control key to select multiple dots at a time</li>
			<li>Move selected dots by dragging the mouse or using the arrow keys</li>
			<li>The Clear button clears the canvas</li>
			<li>The Undo button undoes the last move, the Replay button replays all actions</li>
		</ul>
	<h3>Edges</h3>
		<ul>
			<li>Given a set of selected vertices S, E connects every vertex in S to every vertex in the complement of S</li>
			<li>C clears all edges with one dot in S and one dot in the complement of S</li>
			<li>T toggles all such vertices</li>
			<li>Edges can be selected the same way as dots</li>
			<li>Drag a point on an edge to curve it</li>
		</ul>
	<h3>Properties</h3>
		<ul>
			<li>Add a new property option using the Add Property input box and button</li>
			<li>Set a property for a set of selected dots/edges using the Set Property Value drop-down menus, input box, and button</li>
			<li>Mouse over a dot/edge to show property values</li>
			<li>Captions are a default property; when it is set, it is shown next to the item it is labeling without needing to mouse over</li>
		</ul>
	<h3>Sequence Mode</h3>
		<ul>
			<li>Click the sequence button to enter sequence mode</li>
			<li>In sequence mode, every new dot created will automatically create an edge between the new and previous vertex</li>
			<li>Mode is automatically terminated once the original dot is reached</li>
			<li>Mode is terminated by clicking the button or pressing q</li>
			<li>Escape exits cancelling changes</li>
		</ul>
	<h3>Spoke Mode</h3>
		<ul>
			<li>Click the edge button to enter spoke mode</li>
			<li>In spoke mode, a vertex can be created by clicking on an empty spot</li>
			<li>First, select a vertex(ices) to be the indicated vertex(ices)</li>
			<li>Then, the vertex(ices) that the mouse selects for will toggle edges between the originally indicated vertices, and the latest selected vertices</li>
			<li>Mode is terminated by clicking the button, or pressing q</li>
			<li>Escape exits cancelling changes</li>
		</ul>
	<h3>Algorithms</h3>
		<ul>
			<li>Click the Dijkstra button to find the shortest path between two vertices, taking into account the weights of the edges. If a weight is not set, the edge size is used as default</li>
			<li>The shortest path will be highlighted in red</li>
		</ul>
	<h3>Saving/Retrieving Canvases</h3>
		<ul>
			<li>Press save to save the current canvas</li>
			<li>Press load to load your previously saved canvas</li>
		</ul>
